const mongoose = require('mongoose');
const Schema = mongoose.Schema;

//Create schema
const NoteSchema = new Schema({
  title: {
    type: String,
    required: true
  },
  body: {
    type: String,
    required: true
  },
  user: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

mongoose.model('notes', NoteSchema);